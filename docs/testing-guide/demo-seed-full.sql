-- =====================================================================
-- VAiyu Testing Guide — FULL demo seed (operational catalog + journey)
-- =====================================================================
-- Run this AFTER dashboard-demo-seed.sql (which creates the in-house guest
-- Asha Verma, the arriving guest, the future booking, folio + payment).
--
-- This script adds everything needed to make the guest/ops/kitchen screens
-- show live data:
--   • Departments + Services catalog (Housekeeping / Room Service / Maintenance)
--   • A small food menu (Main Course + Beverages)
--   • Links the dev-owner auth user to Asha's guest record (so the Guest Portal
--     shows her stay in the dev session) — DEV CONVENIENCE ONLY.
--   • 2 service requests (tickets) + 1 food order on Asha's stay
--   • Legacy `orders` rows that drive the dashboard Revenue/ADR KPIs
--   • Refreshes the owner_dashboard_kpis materialized view
--
-- Idempotent: safe to run more than once. Looks up Asha's stay dynamically,
-- so it keeps working after a DB reset + re-seed (where IDs are regenerated).
--
-- Apply locally:
--   docker exec -i supabase_db_<ref> psql -U postgres -d postgres \
--     < docs/testing-guide/demo-seed-full.sql
-- =====================================================================

DO $$
DECLARE
  v_hotel uuid := '00000000-0000-0000-0000-0000000000d2';   -- Dev Test Hotel
  v_owner uuid := '00000000-0000-0000-0000-0000000000d1';   -- dev-owner@vaiyu.test
  v_stay  uuid;
  v_guest uuid;
  v_room  uuid;
  -- fixed catalog ids (valid hex) so re-runs don't duplicate
  d_hk uuid := '00000000-0000-0000-0000-00000000de01';
  d_rs uuid := '00000000-0000-0000-0000-00000000de02';
  d_mt uuid := '00000000-0000-0000-0000-00000000de03';
  s_tow uuid := '00000000-0000-0000-0000-00000000ce01';
  s_cln uuid := '00000000-0000-0000-0000-00000000ce02';
  s_ac  uuid := '00000000-0000-0000-0000-00000000ce03';
  mc_main uuid := '00000000-0000-0000-0000-00000000ca01';
  mc_bev  uuid := '00000000-0000-0000-0000-00000000ca02';
  mi_thali  uuid := '00000000-0000-0000-0000-0000000fa001';
  mi_paneer uuid := '00000000-0000-0000-0000-0000000fa002';
  mi_chai   uuid := '00000000-0000-0000-0000-0000000fa003';
  mi_lime   uuid := '00000000-0000-0000-0000-0000000fa004';
  v_code text;
  v_fo uuid;
BEGIN
  -- Locate the seeded in-house demo stay (Asha Verma) dynamically
  SELECT s.id, s.guest_id, s.room_id, s.booking_code
    INTO v_stay, v_guest, v_room, v_code
  FROM public.stays s
  JOIN public.guests g ON g.id = s.guest_id
  WHERE s.hotel_id = v_hotel AND s.status = 'inhouse' AND g.full_name LIKE 'DEMO%'
  ORDER BY s.actual_checkin_at DESC NULLS LAST
  LIMIT 1;

  IF v_stay IS NULL THEN
    RAISE EXCEPTION 'No in-house DEMO stay found. Run dashboard-demo-seed.sql first.';
  END IF;

  -- Link dev-owner -> Asha so the Guest Portal renders her stay (dev only)
  INSERT INTO public.guest_user_map (user_id, guest_id)
  VALUES (v_owner, v_guest)
  ON CONFLICT (user_id) DO UPDATE SET guest_id = EXCLUDED.guest_id;

  -- Catalog: departments
  INSERT INTO public.departments (id, hotel_id, name, code, is_active, display_order, is_custom, template_id)
  VALUES (d_hk, v_hotel,'Housekeeping','HK', true,1,true,NULL),
         (d_rs, v_hotel,'Room Service','RS', true,2,true,NULL),
         (d_mt, v_hotel,'Maintenance','MT', true,3,true,NULL)
  ON CONFLICT (id) DO NOTHING;

  -- Catalog: services
  INSERT INTO public.services (id, hotel_id, key, label, sla_minutes, active, department_id, is_custom, template_id)
  VALUES (s_tow, v_hotel,'extra_towels','Extra Towels',20,true,d_hk,true,NULL),
         (s_cln, v_hotel,'room_cleaning','Room Cleaning',30,true,d_hk,true,NULL),
         (s_ac,  v_hotel,'ac_repair','AC Repair',45,true,d_mt,true,NULL)
  ON CONFLICT (id) DO NOTHING;

  -- Catalog: menu
  INSERT INTO public.menu_categories (id, hotel_id, key, name, display_order, active)
  VALUES (mc_main, v_hotel,'main','Main Course',1,true),
         (mc_bev,  v_hotel,'bev','Beverages',2,true)
  ON CONFLICT (id) DO NOTHING;

  INSERT INTO public.menu_items (id, hotel_id, item_key, name, active, price, is_veg, category_id, metadata, display_order)
  VALUES (mi_thali, v_hotel,'veg_thali','Veg Thali', true,220,true,mc_main,'{}'::jsonb,1),
         (mi_paneer,v_hotel,'paneer_bm','Paneer Butter Masala', true,280,true,mc_main,'{}'::jsonb,2),
         (mi_chai,  v_hotel,'masala_chai','Masala Chai', true,40,true,mc_bev,'{}'::jsonb,3),
         (mi_lime,  v_hotel,'lime_soda','Fresh Lime Soda', true,60,true,mc_bev,'{}'::jsonb,4)
  ON CONFLICT (id) DO NOTHING;

  -- Service requests (display_id auto-generated by trigger)
  IF NOT EXISTS (SELECT 1 FROM public.tickets WHERE stay_id=v_stay AND title='Extra towels for Room 201') THEN
    INSERT INTO public.tickets (service_department_id, service_id, title, description, status, priority, created_by_type, created_by_id, hotel_id, room_id, stay_id, created_at)
    VALUES (d_hk, s_tow,'Extra towels for Room 201','Guest requested 2 extra bath towels.','NEW','NORMAL','GUEST',v_guest,v_hotel,v_room,v_stay, now()-interval '8 minutes');
  END IF;
  IF NOT EXISTS (SELECT 1 FROM public.tickets WHERE stay_id=v_stay AND title='AC not cooling in Room 201') THEN
    INSERT INTO public.tickets (service_department_id, service_id, title, description, status, priority, created_by_type, created_by_id, hotel_id, room_id, stay_id, created_at)
    VALUES (d_mt, s_ac,'AC not cooling in Room 201','Air conditioner blowing warm air.','IN_PROGRESS','HIGH','GUEST',v_guest,v_hotel,v_room,v_stay, now()-interval '25 minutes');
  END IF;

  -- Food order (modern food_orders; display_id auto via trigger)
  IF NOT EXISTS (SELECT 1 FROM public.food_orders WHERE stay_id=v_stay AND special_instructions='Less spicy, room 201') THEN
    v_fo := gen_random_uuid();
    INSERT INTO public.food_orders (id, hotel_id, stay_id, room_id, status, total_amount, currency, special_instructions, created_at, updated_at)
    VALUES (v_fo, v_hotel, v_stay, v_room,'PREPARING',300,'INR','Less spicy, room 201', now()-interval '10 minutes', now());
    INSERT INTO public.food_order_items (id, food_order_id, menu_item_id, item_name, quantity, unit_price, total_price, modifiers, status, created_at, updated_at)
    VALUES (gen_random_uuid(), v_fo, mi_thali,'Veg Thali',1,220,220,'{}'::jsonb,'PREPARING', now(), now()),
           (gen_random_uuid(), v_fo, mi_chai,'Masala Chai',2,40,80,'{}'::jsonb,'PREPARING', now(), now());
  END IF;

  -- Legacy `orders` rows that feed the dashboard Revenue/ADR KPIs
  INSERT INTO public.orders (hotel_id, booking_code, item_key, qty, price, status, room, created_at)
  SELECT v_hotel, v_code, v.item_key, v.qty, v.price, v.status::order_status, '201', now()
  FROM (VALUES ('VEG-THALI',1,220.00,'delivered'),
               ('MASALA-CHAI',2,40.00,'delivered'),
               ('PANEER-BUTTER-MASALA',1,280.00,'open')) AS v(item_key,qty,price,status)
  WHERE NOT EXISTS (SELECT 1 FROM public.orders o WHERE o.room='201' AND o.item_key=v.item_key);

  RAISE NOTICE 'Full demo seed complete for stay % (room 201).', v_stay;
END $$;

-- KPI cache must be refreshed for Revenue/Occupancy to render (and to clear the
-- "materialized view not populated" 500 on a fresh database).
REFRESH MATERIALIZED VIEW public.owner_dashboard_kpis;
